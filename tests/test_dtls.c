MINUNIT_TEST(test_dtls_init_generates_certificate_and_fingerprint)
{
    dtls_context_t *dtls;
    char fingerprint[DTLS_FINGERPRINT_MAX_LEN * 3];
    int fp_len;

    memset(fingerprint, 0, sizeof(fingerprint));

    dtls = dtls_init(DTLS_ROLE_CLIENT);
    MINUNIT_ASSERT(dtls != NULL, "Expected DTLS init to generate a local certificate");
    MINUNIT_ASSERT(dtls->cert != NULL, "Expected DTLS certificate to be present");
    MINUNIT_ASSERT(dtls->pkey != NULL, "Expected DTLS private key to be present");

    fp_len = dtls_get_fingerprint(dtls, fingerprint, sizeof(fingerprint));
    MINUNIT_ASSERT(fp_len > 0, "Expected DTLS fingerprint to be generated");
    MINUNIT_ASSERT(strchr(fingerprint, ':') != NULL,
                   "Expected DTLS fingerprint to use colon-separated hex format");

    dtls_destroy(dtls);
    return 0;
}

MINUNIT_TEST(test_dtls_start_configures_bio_callbacks)
{
    dtls_context_t *dtls;
    ice_session_t ice;
    ice_check_pair_t pair;
    ice_candidate_internal_t local;
    ice_candidate_internal_t remote;
    tinyrtc_error_t err;

    memset(&ice, 0, sizeof(ice));
    memset(&pair, 0, sizeof(pair));
    memset(&local, 0, sizeof(local));
    memset(&remote, 0, sizeof(remote));

    pair.local = &local;
    pair.remote = &remote;
    pair.succeeded = true;
    strcpy(remote.ip, "127.0.0.1");
    /* This captured HelloVerifyRequest/ClientHello pair came from a runtime
     * exchange where the client-side ICE port was 60034. DTLS cookies are
     * bound to the transport ID, so the test must use the same port. */
    remote.port = 60034;

    ice.socket = 9;
    ice.selected_pair = &pair;

    dtls = dtls_init(DTLS_ROLE_CLIENT);
    MINUNIT_ASSERT(dtls != NULL, "Expected DTLS init to succeed before start");

    err = dtls_start(dtls, &ice);
    MINUNIT_ASSERT(err == TINYRTC_OK, "Expected dtls_start to succeed");
    MINUNIT_ASSERT(dtls->ssl->f_send != NULL, "Expected DTLS send BIO callback to be configured");
    MINUNIT_ASSERT(dtls->ssl->f_recv != NULL, "Expected DTLS recv BIO callback to be configured");
    MINUNIT_ASSERT(dtls->ssl->f_set_timer != NULL, "Expected DTLS timer callback to be configured");
    MINUNIT_ASSERT(dtls->ssl->f_get_timer != NULL, "Expected DTLS timer callback to be configured");

    dtls_destroy(dtls);
    return 0;
}

