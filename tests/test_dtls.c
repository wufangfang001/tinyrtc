MINUNIT_TEST(test_dtls_export_keys_callback_captures_master_secret)
{
    dtls_context_t *dtls;
    unsigned char master_secret[48];
    int ret;

    for (size_t i = 0; i < sizeof(master_secret); i++) {
        master_secret[i] = (unsigned char)(0x30 + i);
    }

    dtls = dtls_init(DTLS_ROLE_CLIENT);
    MINUNIT_ASSERT(dtls != NULL, "Expected DTLS init to succeed");
    MINUNIT_ASSERT(dtls->ctx->f_export_keys != NULL,
                   "Expected DTLS config to install key export callback");

    ret = dtls->ctx->f_export_keys(dtls->ctx->p_export_keys,
                                   master_secret,
                                   NULL,
                                   0,
                                   0,
                                   0);
    MINUNIT_ASSERT(ret == 0, "Expected DTLS export callback to succeed");
    MINUNIT_ASSERT(dtls->master_secret_captured,
                   "Expected DTLS export callback to mark master secret as captured");
    MINUNIT_ASSERT(memcmp(dtls->master_secret, master_secret, sizeof(master_secret)) == 0,
                   "Expected DTLS export callback to copy the master secret bytes");

    dtls_destroy(dtls);
    return 0;
}

